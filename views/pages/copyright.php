  	<h4>How to report claims of copyright infringement by users</h4>
  	<p>
  		Miio complies with the Digital Millennium Copyright Act (17 U.S.C. ยง 512), also known as the DMCA. The DMCA provides a simple procedure for copyright owners 
  		to report content that infringes their copyrights. Please be aware that the DMCA makes copyright owners liable if they materially misrepresent that a user's 
  		content is infringing.
  	</p>
  	<p>
  		To report a copyright infringement by a Miio user, all you need to do is fill out our <a href="pages/dmca">automated DMCA form</a>. This form is the fastest way to report a copyright infringement.
  	</p>
